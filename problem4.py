num1 = int(input("Please enter number 1 "))
num2 = int(input("Please enter number 2 "))
operator = input("What calculation would you like to do? (add, sub, mult, div) ")

if operator == "add":
	print(num1 + num2)
elif operator == "sub":
	print(num1 - num2)
elif operator == "mult":
	print(num1 * num2)
elif operator == "div":
	print(num1 / num2)





